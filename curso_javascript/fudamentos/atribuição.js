// operadores de atribuição,são operadores que atribuir valores a variaveis o operador mais comum para atribuir valores é o =,mas temos outros operadores,como os operadores aritmeticos 
const a = 7
let b = 3
// atribuição de soma 
console.log(b+= a) // isso aqui seria a mesma coisa de (b = b + a )
// atribuição subtrativa 
b -= 4 // (b = b - 4)
console.log(b)

// atribuição multiplicativa 
b *= 2 // (b = b * 2)
console.log(b)

// atribuição divisiva 
b /= 2 //(b = b / 2)
console.log(b)
// podemos usar o % para descobrir se um numero é par ou impar,se o resultador for 0 o numero é par,se o resultador for 1 o numero é impar  
b %= 2 // (b = b % 2 )
console.log(b)

